    private void icoPrintMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_icoPrintMouseClicked
        if (notSelected) {
            JOptionPane.showMessageDialog(null, "There's no Ticket Data Selected!");
        } else {
            int idx = tblTransaksi.getSelectedRow();
            String idxdata = (tblTransaksi.getModel().getValueAt(idx, 0).toString());
            if (idxdata.equals("")){
                JOptionPane.showMessageDialog(null, "Ticket Data is null!");
            } else {
                f_Ticket tiket = new f_Ticket();
                tiket.setVisible(true);
                tiket.pack();
                this.dispose();
            }
        }
    }//GEN-LAST:event_icoPrintMouseClicked

