    private void tblTransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTransaksiMouseClicked
        try {
            int idx = tblTransaksi.getSelectedRow();
            String idxdata = (tblTransaksi.getModel().getValueAt(idx, 0).toString());
            c_Transaksi t = new c_Transaksi();
            t.storeTransaksi(idxdata);
            notSelected = false;
        } catch (SQLException ex) {
            Logger.getLogger(f_Transaksi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblTransaksiMouseClicked

